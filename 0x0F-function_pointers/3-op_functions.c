#include "3-calc.h"

/**
 * op_add - function adds two integer
 * @a: first number
 * @b: second number
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function subtracts two integer
 * @a: first number
 * @b: second number
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function multiples two integer
 * @a: first number
 * @b: second number
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function divides two integer
 * @a: first number
 * @b: second number
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function calculate modules two integer
 * @a: first number
 * @b: second number
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
